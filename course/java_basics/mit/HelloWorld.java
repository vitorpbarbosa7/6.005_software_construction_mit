// creating a public class 
public class HelloWorld {
    // creating main method to be accessible outside of the class 
    
    // main method which is static, so no need to instantiate this

    // void : return type (does not return any value)

    // method name, in this case the main method is the one called
    // when compiling
    
    // String[] args allows to pass parameters to this method
    // It was designed this way, to be obliged to have this as entry point
    public static void main(String[] args) {
        System.out.println("Hello World"); 
    }
}